import os
import uuid
from django.db import models
from django.utils import timezone
from django.utils.text import slugify
from django.contrib.auth import get_user_model
from django.core.validators import FileExtensionValidator

User = get_user_model()


class BaseModel(models.Model):
    created_at = models.DateTimeField(auto_now_add=True, db_index=True)
    updated_at = models.DateTimeField(auto_now=True, db_index=True)

    class Meta:
        abstract = True
        ordering = ["-created_at"]


def blog_image_upload_path(instance, filename):
    ext = "webp"
    unique_name = f"{uuid.uuid4().hex}.{ext}"
    return os.path.join("blog", "images", str(timezone.now().year), str(timezone.now().month), unique_name)


def blog_thumbnail_upload_path(instance, filename):
    ext = "webp"
    unique_name = f"{uuid.uuid4().hex}_thumb.{ext}"
    return os.path.join("blog", "images", str(timezone.now().year), str(timezone.now().month), unique_name)


def blog_medium_upload_path(instance, filename):
    ext = "webp"
    unique_name = f"{uuid.uuid4().hex}_medium.{ext}"
    return os.path.join("blog", "images", str(timezone.now().year), str(timezone.now().month), unique_name)


class Category(BaseModel):
    name = models.CharField(max_length=200, unique=True, db_index=True)
    slug = models.SlugField(max_length=220, unique=True, db_index=True, blank=True)
    description = models.TextField(blank=True)
    meta_title = models.CharField(max_length=160, blank=True)
    meta_description = models.CharField(max_length=320, blank=True)
    is_active = models.BooleanField(default=True, db_index=True)

    class Meta:
        verbose_name = "Category"
        verbose_name_plural = "Categories"
        ordering = ["name"]

    def __str__(self):
        return self.name

    def save(self, *args, **kwargs):
        if not self.slug:
            self.slug = self._generate_unique_slug()
        super().save(*args, **kwargs)

    def _generate_unique_slug(self):
        base_slug = slugify(self.name)
        slug = base_slug
        counter = 1
        while Category.objects.filter(slug=slug).exclude(pk=self.pk).exists():
            slug = f"{base_slug}-{counter}"
            counter += 1
        return slug


